def f(node,h):
    if node is None:
        return 0
    ht = max(1+f(node.left,h), 1+f(node.right,h) ) 
    return ht


def height(root):
    h = max (f(root.left,0), f(root.right,0))
    #print("height={}".format(h))
    return h